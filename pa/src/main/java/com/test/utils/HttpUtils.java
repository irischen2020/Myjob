	/**
	 * 发送 GET 请求（HTTP），K-V形式
	 *
	 * @param url
	 * @param params
	 * @return
	 */
	public static String doGet(String url, Map<String, Object> params) {
		String apiUrl = url;
		StringBuffer param = new StringBuffer();
		//拼接get请求参数
		int i = 0;
		for (String key : params.keySet()) {
			if (i == 0)
				param.append("?");
			else
				param.append("&");
			param.append(key).append("=").append(params.get(key));
			i++;
		}
		//往url后面添加所有的请求参数
		apiUrl += param;
		String result = null;
		//开始执行get请求，新建一个DefaultHttpClient对象
//		HttpClient httpclient = new DefaultHttpClient();//DefaultHttpClient被弃用了
		HttpClient httpClient = HttpClientBuilder.create().build();//获取DefaultHttpClient请求
		try {
			//新建一个处理特定url的HttpGet对象
			HttpGet httpGet = new HttpGet(apiUrl);
			//执行请求，得到response对象
			HttpResponse response = httpClient.execute(httpGet);
			//获取响应状态码
			int statusCode = response.getStatusLine().getStatusCode();
			
			System.out.println("执行状态码 : " + statusCode);
			
			HttpEntity entity = response.getEntity();
			if (entity != null) {
				//处理结果
				// result = EntityUtils.toString(entity, "UTF-8");
				InputStream instream = entity.getContent();
				result = inputStream2String(instream, "UTF-8");
			}
		} catch (IOException e) {
			e.printStackTrace();
		}
		return result;
	}
	
	//inputStream to String
	private static String inputStream2String(InputStream is, String encode) {
		String result = "";
		try {
			if (encode == null || encode.equals("")) {
				// 默认以utf-8形式
				encode = "utf-8";
			}
			BufferedReader reader = new BufferedReader(new InputStreamReader(is, encode));
			StringBuffer sb = new StringBuffer();
			
			while ((result = reader.readLine()) != null) {
				sb.append(result).append("\n");
			}
			return sb.toString();
		} catch (UnsupportedEncodingException e1) {
			e1.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}
		
		return result;
	}
	
